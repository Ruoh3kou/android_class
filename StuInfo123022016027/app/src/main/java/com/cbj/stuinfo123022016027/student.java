package com.cbj.stuinfo123022016027;

public class student {
    public String name;
    public String sex;
    public String mobile;
    public String address;
    public String no;

    student() {
        name = "";
        sex = "";
        mobile = "";
        address = "";
        no = "";
    }

    student(String n, String s, String m, String a, String o) {
        name = n;
        sex = s;
        mobile = m;
        address = a;
        no = o;
    }
}
